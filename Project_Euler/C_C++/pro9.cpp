#include <iostream>

using namespace std;

int main()
{
	for (int a = 1; a < 333; a++)
	{
		for (int b = a + 1; b < 666; b++)
		{
			for (int c = b+1; c < 999; c++)
			{
				if (a+b+c!=1000)
				{
					continue;
				}
				else
				{
					if (a*a+b*b==c*c)
					{
						cout<<"Result: "<<a*b*c<<endl;
						break;
					}
				}		 
			}
		}
	}
}

